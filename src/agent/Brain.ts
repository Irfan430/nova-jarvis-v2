export function getWelcomeMessage(userName: string = "Boss"): string {
  const hour = new Date().getHours();
  let greeting = "শুভ সন্ধ্যা";
  if (hour >= 5 && hour < 12) greeting = "শুভ সকাল";
  else if (hour >= 12 && hour < 17) greeting = "শুভ দুপুর";
  else if (hour >= 17 && hour < 20) greeting = "শুভ বিকাল";

  return `
╔══════════════════════════════════════════════════════════════╗
║          Nova Jarvis v2 — Online and Ready                   ║
╚══════════════════════════════════════════════════════════════╝

${greeting}, ${userName}। আমি Nova — আপনার ব্যক্তিগত AI এজেন্ট।

আমি প্রস্তুত। আপনি যা চান বলুন — ফাইল ম্যানেজ করা, কোড লেখা,
ওয়েব সার্চ, সিস্টেম কন্ট্রোল, বা যেকোনো কাজ।

টাইপ করুন 'help' — সব কমান্ড দেখতে।
টাইপ করুন 'exit' — বন্ধ করতে।
`.trim();
}
